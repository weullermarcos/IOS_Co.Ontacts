- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.names = [[NSMutableArray alloc] init];
    [self.names  addObject:@"Paulo"];
    [self.names  addObject:@"Jonatas"];
    [self.names  addObject:@"Pedro"];
    [self.names  addObject:@"Augusto"];
    [self.names  addObject:@"Hoff"];
    
}

